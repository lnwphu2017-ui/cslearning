def clean_text(text):
    for old, new in REPLACEMENTS.items():
        text = text.replace(old, new)
    
    # Header specific fixes
    text = re.sub(r'ระบบจัดการไฟล์ \(File Systems\): ระบบการจัดการไฟล์และการจัดระเบียบข้อมูล', 'ระบบจัดการไฟล์ (File Systems) และการจัดระเบียบข้อมูล', text)
    text = re.sub(r'โครงสร้างภายในและวงจร Fetch-Decode-Execute \(วงจรการทำงานพื้นฐานของระบบ\)', 'โครงสร้างภายในและวงจร Fetch-Decode-Execute', text)
    text = re.sub(r'Cache Memory: การจัดการความต่างของความเร็วด้วย Cache Memory', 'หน่วยความจำแคช (Cache Memory) และการเพิ่มความเร็วในการเข้าถึงข้อมูล', text)
    text = re.sub(r'มาตรฐาน IEEE 754: ข้อจำกัดและการแทนค่าตัวเลขทศนิยม', 'มาตรฐาน IEEE 754 และการแทนค่าตัวเลขทศนิยม', text)

    return text

def update_lessons(json_path):
    with open(json_path, 'r', encoding='utf-8') as f:
        data = json.load(f)
    
    if 'year1' not in data:
        return

    for course_slug, lessons in data['year1'].items():
        for lesson in lessons:
            if 'content' in lesson:
                lesson['content'] = clean_text(lesson['content'])
                
    with open(json_path, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

